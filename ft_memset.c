#include "libft.h"

void		*ft_memset(void *ptr, int x, size_t n)
{
	size_t	i;
	char	*a;

	i = 0;
	a = ptr;
	while (i < n)
	{
		a[i] = (unsigned char)x;
		i++;
	}
	return (&a[0]);
}
